  static Future<Surah> getSurahByNumber(int number) async {
    final surahs = await getAllSurahs();
    return surahs.firstWhere(
      (surah) => surah.number == number,
      orElse: () => throw Exception('Surah not found'),
    );
  }

  static Future<Surah> getSurahByName(String name) async {
    final surahs = await getAllSurahs();
    return surahs.firstWhere(
      (surah) => surah.name == name,
      orElse: () => throw Exception('Surah not found'),
    );
  }
